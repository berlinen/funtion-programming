
// Symbol 列表
// 一种方法，用于确定构造函数对象是否将对象识别为构造函数的实例之一。由instanceof运算符的语义调用。
Symbol.hasInstance
// 一个布尔值，指示对象应通过Array.prototype.concat展平为其数组元素
Symbol.isConcatSpreadable
// 返回对象的默认迭代器的方法。由for-of语句的语义调用。
Symbol.iterator
// 一种正则表达式方法，它将正则表达式与字符串相匹配。通过该String.prototype.match方法调用。
Symbol.match
// 一种正则表达式方法，用于替换字符串的匹配子字符串。通过该String.prototype.replace方法调用。
Symbol.replace
// 一种正则表达式方法，用于返回与正则表达式匹配的字符串中的索引。通过该String.prototype.search方法调用。
Symbol.search
// 一种正则表达式方法，用于在与正则表达式匹配的索引处拆分字符串。通过该String.prototype.split方法调用。
Symbol.split
// 一种将对象转换为对应的原始值的方法。由ToPrimitive抽象操作调用。
Symbol.toPrimitive
//用于创建对象的默认字符串描述的String值。由内置方法调用Object.prototype.toString。
Symbol.toStringTag
//一个Object，其自身的属性名称是从关联对象的“with”环境绑定中排除的属性名称。
Symbol.unscopables




